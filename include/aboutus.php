<p>X Store is a leading online Ecommerce store that sells Men and Women clothing and accessories. Their goals to develop Ecommerce website is to sell their top products through online ecommerce website.</p>
<p>
X Store is an Ecommerce website platform that is primarily developed using Presta Shop, an Open Source Development platform chosen for its highly integrated functionality of choosing and integrating product attributes like size, color, material
and brand preferences. As we wanted X Store to give extra ordinary functionality and user interface which should be easy
for its stakeholders and administrator to keep track of sales and products, we opted Presta Shop for this reason.
